# 範圍內質數查詢程式
# 要求使用者輸入開始和結束的整數
開始數 = int(input("請輸入開始整數: "))
結束數 = int(input("請輸入結束整數: "))

# 用迴圈逐一檢查開始數到結束數之間的每個數字
for 數字 in range(開始數, 結束數 + 1):
    # 只檢查大於 1 的數字
    if 數字 > 1:
        # 先假設這個數字是質數
        是質數 = True
        # 從 2 檢查到 數字的平方根
        # 這樣可以更快地檢查是否有整除因子
        for 除數 in range(2, int(數字**0.5) + 1):
            # 如果這個數字能被整除
            if 數字 % 除數 == 0:
                # 那它就不是質數
                是質數 = False
                # 提早結束迴圈
                break
        # 如果是質數就輸出
        if 是質數:
            print(數字)
