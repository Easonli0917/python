# 菱形圖案繪制程式
# 要求使用者輸入菱形的大小
b = input("請輸入你要的菱形大小:")
# 將字串轉換為整數
b = int(b)
# 上半部的菱形（上寬下窄）
for i in range(b):
    # 每一行首先打印 (b - i - 1) 個空白，以保持居中
    # 然後打印 (2 * i + 1) 個星號
    print(" " * (b - i - 1) + "*" * (2 * i + 1))
# 下半部的菱形（下寬上窄）
for i in range(b):
    # 打印下半部，每行打印 (b - 1) 個空白和 1 個星號
    print(" " * (b - 1) + "*" * 1)
