# 倍數查詢程式
# 要求使用者輸入第一個數字
n = input("請輸入一個數字:")
# 要求使用者輸入第二個數字
t = input("請再輸入一個數字:")
# 要求使用者輸入查詢範圍的上限
th = input("請再輸入一個數字:")
# 將字串轉換為整數
n = int(n)
# 將字串轉換為整數
t = int(t)
# 將字串轉換為整數
th = int(th)
# 用迴圈逐一檢查從 1 到 th 範圍內的每個數字
for i in range(1, th + 1):
    # 如果 i 是 n 的倍數或 t 的倍數
    if i % n == 0 or i % t == 0:
        # 就輸出這個數字
        print(i)
