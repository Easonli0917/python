# BMI 計算程式
# 要求使用者輸入身高（公分）
a = int(input("請輸入身高(公分):"))
# 要求使用者輸入體重（公斤）
b = int(input("請輸重量(公斤):"))
# 計算 BMI 值
# 公式：體重(kg) / 身高(m)的平方
# 需要把身高從公分轉換為公尺
i = b / ((a / 100) ** 2)
# 根據 BMI 值判斷體重狀況
# BMI < 18.5 表示體重過輕
if i < 18.5:
    print(f"你的BMI值為:{i}")
    print("體重過輕")
# BMI >= 25 表示體重過重
elif i >= 25:
    print(f"你的BMI值為:{i}")
    print("體重過重")
# BMI 介於 18.5 ~ 24.9 表示體重正常
else:
    print(f"你的BMI值為:{i}")
    print("體重正常")
