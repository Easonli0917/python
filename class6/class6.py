# while迴圈範例
# 建立計數變數 i，初始值為 0
# 當 i < 10 時，不斷執行迴圈內的程式
# 每回合結束時，用 += 運算子將 i 增加 1
i = 0
while i < 10:
    print(i)
    i += 1

# 指定運算子（複合指定運算子）
# +=，-=，*=，/=，//=，%=，**=
# x = x + 1 等於 x += 1

# 密碼驗證程式
# 要求使用者輸入密碼
# 建立計數變數 s，用來記錄輸入次數
n = input("請輸入一個數字:")
s = 1
# while迴圈，當輸入不等於正確密碼 1234 時，持續要求重新輸入
while n != "1234":
    n = input("請再輸入一個數字:")
    s += 1
    # 當輸入次數達到 3 次時，顯示系統鎖定訊息
    if s >= 3:
        print("你已經輸入三次錯誤的密碼，系統鎖定")
# 當密碼正確或系統結束後，顯示成功訊息
print("密碼正確，歡迎使用本系統")
